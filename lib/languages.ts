export function getTranslation(language: string, key: string, params?: Record<string, string>): string {
  const lang = translations[language as keyof typeof translations] || translations.en
  let text = lang[key as keyof typeof lang] || translations.en[key as keyof typeof translations.en] || key

  if (params) {
    Object.entries(params).forEach(([param, value]) => {
      text = text.replace(`{${param}}`, value)
    })
  }

  return text
}